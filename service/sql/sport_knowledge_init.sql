-- ===================== 运动知识库建表 =====================
CREATE TABLE IF NOT EXISTS sport_knowledge (
    id BIGINT PRIMARY KEY AUTO_INCREMENT,
    category VARCHAR(32) NOT NULL COMMENT '分类：禁忌/损伤/营养/训练/心率/热身/恢复',
    title VARCHAR(255) NOT NULL COMMENT '标题',
    content TEXT NOT NULL COMMENT '知识内容',
    keywords VARCHAR(500) COMMENT '关键词（逗号分隔）',
    source VARCHAR(64) DEFAULT 'manual' COMMENT '来源',
    create_time DATETIME DEFAULT CURRENT_TIMESTAMP,
    update_time DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    INDEX idx_category (category),
    INDEX idx_keywords (keywords(100))
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='运动知识库';

